public class Point{

    private double x;
    private double y;
    private double z;

    public Point(double a,double b,double c){

        x=a;
        y=b;
        z=c;
    }


    public double getX(){

        return x;
    }
    public double getY(){

        return y;
    }
    public double getZ(){
        return z;
    }
}